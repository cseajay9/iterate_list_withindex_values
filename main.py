# iterating list with its values and index no.

# iterating list to get list item
our_list = ['apple', 'mango', 'banana', 'cherry', 'kiwi']
for values in our_list:
    print(values, " ")

# iterating through list to get index no. of list
fruit_list = ['apple', 'banana', 'orange', 'jackfruit', 'sabeda']
for index in range(len(fruit_list)):
    print("at index: ", index, "the list has: ", fruit_list[index])

